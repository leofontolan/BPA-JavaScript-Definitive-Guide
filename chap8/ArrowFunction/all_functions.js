// Sintaxe de função normal
function normalSum(a, b) { 
    return a + b; 
};
console.log(normalSum(1, 2));

// Sintaxe de função anônima
const anonymousSum = function(a, b) { 
    return a + b; 
}
console.log(anonymousSum(1, 1));

// Sintaxe de arrow function
const arrowSum = (a, b) => {
    return a + b;
};
console.log(arrowSum(2,2));