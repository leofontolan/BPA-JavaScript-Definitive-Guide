// Arrow Function com apenas um parâmetro com parenteses
const double = (x) => x * 2;

console.log(double(2));


// Arrow Function com apenas um parâmetro sem  parenteses
const triple = x => x * 3;

console.log(triple(2));

//Enganadinha do underline
const quadruple = _ => _ * 4;
console.log(quadruple(2));